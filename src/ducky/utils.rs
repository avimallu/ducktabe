pub const fn simplify_arrow_type(name: &str) -> Option<&'static str> {
    match name.as_bytes() {
        b"Null" => Some("null"),
        b"Boolean" => Some("bool"),
        b"Int8" => Some("i8"),
        b"Int16" => Some("i16"),
        b"Int32" => Some("i32"),
        b"Int64" => Some("i64"),
        b"UInt8" => Some("u8"),
        b"UInt16" => Some("u16"),
        b"UInt32" => Some("u32"),
        b"UInt64" => Some("u64"),
        b"Float16" => Some("f16"),
        b"Float32" => Some("f32"),
        b"Float64" => Some("f64"),
        b"Timestamp" => Some("TS"),
        b"Date32" => Some("DT32"),
        b"Date64" => Some("DT64"),
        b"Time32" => Some("T32"),
        b"Time64" => Some("T64"),
        b"Duration" => Some("DUR"),
        b"Interval" => Some("INT"),
        b"Binary" => Some("BIN"),
        b"FixedSizeBinary" => Some("FSB"),
        b"LargeBinary" => Some("LBIN"),
        b"BinaryView" => Some("BINV"),
        b"Utf8" => Some("utf8"),
        b"LargeUtf8" => Some("Lutf8"),
        b"Utf8View" => Some("utf8V"),
        b"List" => Some("list"),
        b"ListView" => Some("listV"),
        b"FixedSizeList" => Some("FSlist"),
        b"LargeList" => Some("Llist"),
        b"LargeListView" => Some("LlistV"),
        b"Struct" => Some("Struct"),
        b"Union" => Some("Union"),
        b"Dictionary" => Some("Dict"),
        b"Decimal32" => Some("D32"),
        b"Decimal64" => Some("D64"),
        b"Decimal128" => Some("D128"),
        b"Decimal256" => Some("D256"),
        b"Map" => Some("Map"),
        b"RunEndEncoded" => Some("REE"),
        _ => None,
    }
}
